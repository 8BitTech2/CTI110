# Computing an average

avg_sales = 0

num_sales1 = int(input('Enter # of Sales on Day 1:'))
num_sales2 = int(input('Enter # of Sales on Day 2:'))
num_sales3 = int(input('Enter # of Sales on Day 3:'))

# formula for avg of 3 days
avg_sales = (num_sales1 + num_sales2 + num_sales3) / 3

print()
print(f'Average sales over 3 days: {avg_sales:.2f}')

                 
