# If-else statements - car year
car_year = int(input ('Year of vehicle:'))

if car_year < 1967:  # If car_year is before 1967
    print ('Probably has few safety features.')

if car_year > 1970:  # If after 1970
    print ('Probably has seat belts.')

if car_year > 1990:  # If after 1990
    print ('Probably has electronic stability control.')

if car_year > 2002:  # If after 2002
    print ('Probably has airbags.')
    
