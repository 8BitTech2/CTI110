# A program can get an input value from the keyboard.

print('Enter name of best friend:', end=' ')
best_friend = input()
print('My best friend is', best_friend)



# Using int() to convert strings to integers.
my_string = '123'
my_int = int('123')

print(my_string)
print(my_int)


# Converting user input to integers.
print('Enter wage:', end=' ')
wage = int(input())

new_wage = wage + 10
print('New wage:', new_wage)


# The ' character is printed by the two character sequence \'
# Ex: print('\'')
print('   I\'m fine.')

